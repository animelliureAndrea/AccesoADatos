package modelo;

import java.util.Date;

/**
 *
 * @author Andrea
 */
public class Emple {
    int emp_no;
    String apellido;
    String oficio;
    int dir;
    Date fecha_alt;
    double salario;
    double comision;
    int dept_no;

    public Emple(int emp_no, String apellido, String oficio, int dir, Date fecha_alt, double salario, double comision, int dept_no) {
        this.emp_no = emp_no;
        this.apellido = apellido;
        this.oficio = oficio;
        this.dir = dir;
        this.fecha_alt = fecha_alt;
        this.salario = salario;
        this.comision = comision;
        this.dept_no = dept_no;
    }

    public int getEmp_no() {
        return emp_no;
    }

    public void setEmp_no(int emp_no) {
        this.emp_no = emp_no;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getOficio() {
        return oficio;
    }

    public void setOficio(String oficio) {
        this.oficio = oficio;
    }

    public int getDir() {
        return dir;
    }

    public void setDir(int dir) {
        this.dir = dir;
    }

    public Date getFecha_alt() {
        return fecha_alt;
    }

    public void setFecha_alt(Date fecha_alt) {
        this.fecha_alt = fecha_alt;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }

    public double getComision() {
        return comision;
    }

    public void setComision(double comision) {
        this.comision = comision;
    }

    public int getDept_no() {
        return dept_no;
    }

    public void setDept_no(int dept_no) {
        this.dept_no = dept_no;
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("El empleado con número: "+emp_no+" y apellido: "+apellido);
        sb.append(" Su oficio es: "+oficio);
        sb.append(" Su dirección es: "+dir);
        sb.append(" Se dio de alta en la fecha: "+fecha_alt);
        sb.append(" Su salario es: "+salario);
        sb.append(" Su comisión es: "+comision);
        sb.append(" Trabaja en el departamento: "+dept_no);
        return sb.toString();
    }
}
